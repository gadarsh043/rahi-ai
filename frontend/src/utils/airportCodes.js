/**
 * Look up an IATA airport code and return city info, or null.
 * Case-insensitive. Only matches 2-4 char inputs.
 */
export function lookupIATA(query) {
  if (!query || query.length < 2 || query.length > 4) return null;
  const code = query.trim().toUpperCase();
  const match = IATA_TO_CITY[code];
  if (!match) return null;
  return {
    city: match.city,
    country: match.country,
    state: match.state,
    lat: match.lat,
    lng: match.lng,
    display: `${match.city}${match.state ? `, ${match.state}` : ''}, ${match.country}`,
    iata: code,
  };
}
